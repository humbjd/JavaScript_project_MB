let num = 0;

while(num <= 50){
    if(num % 2 == 0){
        console.log(`${num} é par`)
        num++
    }else{
        console.log(`${num} é ímpar`)
        num++
    }
}
        
for(let i = 0; i <= 50; i++){
    if(i % 2 == 0){
        console.log(`${i} é par`)
    }else{
        console.log(`${i} é ímpar`)
    }
}    